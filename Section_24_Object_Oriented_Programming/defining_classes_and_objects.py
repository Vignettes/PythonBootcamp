### What is OOP? ###
#
# OOP is just about using code to recreate things that exist in the world. Such as a car, user, poker hand.
# OOP oriented programming is a method of programming that attempts to model some process or thing in the world as 
# class or object.

# Classes are like blueprints for objects. They can contain methods (functions) and attributes (similar to
#  keys in a dict)

# Instance are objects that are constructed from a class blueprint that contain their class's methods and properties.

